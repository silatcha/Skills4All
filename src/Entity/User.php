    }

    public function serialize()
    {
        return serialize([
            $this->id,
            $this->username,
            $this->password
        ]);
    }

    public function unserialize(string $data)
    {
        list(
            $this->id,
            $this->username,
            $this->password
        )= unserialize($data, ['allowed_classes'=> false]);
    }
}
